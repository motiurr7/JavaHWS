package youTube;

public class ForWhileDoLoop {
	public static void main(String[] args) {
		//for loop
		for (int x=1; x<20; x++) {//1,2,3,4,5,6,7,8,....Bangladesh
			System.out.println("Shumi Akter"+x);
		}
		//while Loop
		int y= 1;
		while (y<=20) {
			System.out.println("Shumi+Palash"+y);
			y=y+2;//1,3,5,7,9,......Shumi+palash.
		}
		int s=1;
		do {
			System.out.println(s);
			int s=s+3;
			
		}while(s<=20);
	}
	

}
